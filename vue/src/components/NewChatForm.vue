<template>
  <form>
    <textarea
      placeholder="メッセージを入力してEnterを押してください"
      v-model="message"
      @keypress.enter.prevent="handleSubmit"
    ></textarea>
  </form>
</template>

<script setup>
import { ref, defineEmits } from 'vue'

const message = ref('')
const emits = defineEmits(['connectCable'])
const handleSubmit = () => {
  emits('connectCable', message.value)
  message.value = ''
}
</script>

